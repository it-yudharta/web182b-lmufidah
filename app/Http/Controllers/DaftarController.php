     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $tambah = new Daftar;
        $tambah->nama = $request['nama'];
        $tambah->address = $request['alamat'];
        $tambah->age = $request['umur'];
        $tambah->save();

        return redirect('/mhs');
    }

